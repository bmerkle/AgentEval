    #region ToMarkdown Tests
    
    [Fact]
    public void ToMarkdown_SingleResult_IncludesAllSections()
    {
        // Arrange
        var result = CreateSampleComparisonResult();
        
        // Act
        var markdown = result.ToMarkdown();
        
        // Assert
        Assert.Contains("# 🔬 Model Comparison Report", markdown);
        Assert.Contains("## 🏆 Winner", markdown);
        Assert.Contains("## 📊 Rankings", markdown);
        Assert.Contains("## 📈 Detailed Metrics", markdown);
        Assert.Contains("## 📉 Statistical Analysis", markdown);
        Assert.Contains("## ⚖️ Scoring Weights", markdown);
        Assert.Contains("Generated by AgentEval", markdown);
    }
    
    [Fact]
    public void ToMarkdown_SingleResult_ContainsWinnerInfo()
    {
        // Arrange
        var result = CreateSampleComparisonResult();
        
        // Act
        var markdown = result.ToMarkdown();
        
        // Assert
        Assert.Contains("**GPT-4o**", markdown); // Winner model name
        Assert.Contains("highest composite score", markdown);
    }
    
    [Fact]
    public void ToMarkdown_WithMinimalOptions_ExcludesOptionalSections()
    {
        // Arrange
        var result = CreateSampleComparisonResult();
        var options = MarkdownExportOptions.Minimal;
        
        // Act
        var markdown = result.ToMarkdown(options);
        
        // Assert
        Assert.Contains("Rankings", markdown);
        Assert.DoesNotContain("Detailed Metrics", markdown);
        Assert.DoesNotContain("Statistical Analysis", markdown);
        Assert.DoesNotContain("Scoring Weights", markdown);
        Assert.DoesNotContain("Generated by AgentEval", markdown);
    }
    
    [Fact]
    public void ToMarkdown_CustomOptions_RespectsSettings()
    {
        // Arrange
        var result = CreateSampleComparisonResult();
        var options = new MarkdownExportOptions
        {
            IncludeHeader = true,
            IncludeWinnerAnnouncement = false,
            IncludeRankingsTable = true,
            IncludeDetailedMetrics = false,
            IncludeStatistics = false,
            IncludeScoringWeights = false,
            IncludeTimestamp = false,
            HeaderEmoji = "🧪"
        };
        
        // Act
        var markdown = result.ToMarkdown(options);
        
        // Assert
        Assert.Contains("🧪", markdown);
        Assert.DoesNotContain("Winner", markdown);
        Assert.Contains("Rankings", markdown);
    }
    
