    private static ModelComparisonResult CreateSampleComparisonResult(string testName = "Sample Test")
    {
        var testCase = new TestCase
        {
            Name = testName,
            Input = "What is 2+2?",
            GroundTruth = "4"
        };
        
        var stats1 = new StochasticStatistics(
            PassRate: 0.95,
            MeanScore: 92.5,
            MedianScore: 94.0,
            StandardDeviation: 3.2,
            MinScore: 85,
            MaxScore: 100,
            Percentile25: 90.0,
            Percentile75: 96.0,
            Percentile95: 99.0,
            ConfidenceInterval: new ConfidenceInterval(90.5, 94.5, 0.95),
            SampleSize: 10);
        
        var stats2 = new StochasticStatistics(
            PassRate: 0.85,
            MeanScore: 85.0,
            MedianScore: 86.0,
            StandardDeviation: 5.5,
            MinScore: 72,
            MaxScore: 95,
            Percentile25: 80.0,
            Percentile75: 90.0,
            Percentile95: 94.0,
            ConfidenceInterval: new ConfidenceInterval(82.0, 88.0, 0.95),
            SampleSize: 10);
        
        var stochasticResult1 = new StochasticResult(
            TestCase: testCase,
            IndividualResults: CreateSampleTestResults(10),
            Statistics: stats1,
            Options: new StochasticOptions(Runs: 10),
            Passed: true);
        
        var stochasticResult2 = new StochasticResult(
            TestCase: testCase,
            IndividualResults: CreateSampleTestResults(10),
            Statistics: stats2,
            Options: new StochasticOptions(Runs: 10),
            Passed: true);
        
        var modelResults = new List<ModelResult>
        {
            new ModelResult(
                ModelId: "gpt-4o",
                ModelName: "GPT-4o",
                StochasticResult: stochasticResult1,
                AverageLatency: TimeSpan.FromMilliseconds(250),
                AverageCost: 0.0025m,
                TotalCost: 0.025m),
            new ModelResult(
                ModelId: "gpt-4o-mini",
                ModelName: "GPT-4o-mini",
                StochasticResult: stochasticResult2,
                AverageLatency: TimeSpan.FromMilliseconds(150),
                AverageCost: 0.0005m,
                TotalCost: 0.005m)
        };
        
        var rankings = new List<ModelRanking>
        {
            new ModelRanking(
                ModelId: "gpt-4o",
                ModelName: "GPT-4o",
                CompositeScore: 88.5,
                QualityScore: 95.0,
                SpeedScore: 70.0,
                CostScore: 60.0,
                ReliabilityScore: 95.0,
                Rank: 1),
            new ModelRanking(
                ModelId: "gpt-4o-mini",
                ModelName: "GPT-4o-mini",
                CompositeScore: 82.0,
                QualityScore: 80.0,
                SpeedScore: 90.0,
                CostScore: 95.0,
                ReliabilityScore: 85.0,
                Rank: 2)
        };
        
        var options = new ModelComparisonOptions
        {
            RunsPerModel = 10,
            EnableCostAnalysis = true,
            EnableStatistics = true
        };
        
        return new ModelComparisonResult(
            TestCase: testCase,
            ModelResults: modelResults,
            Ranking: rankings,
            Winner: rankings[0],
            Options: options);
    }
    
    private static IReadOnlyList<TestResult> CreateSampleTestResults(int count)
    {
        var results = new List<TestResult>();
        for (int i = 0; i < count; i++)
        {
            var perf = new PerformanceMetrics
            {
                StartTime = DateTimeOffset.UtcNow,
                PromptTokens = 50,
                CompletionTokens = 10,
                ModelUsed = "gpt-4o"
            };
            perf.EndTime = perf.StartTime + TimeSpan.FromMilliseconds(200 + i * 10);
            
            results.Add(new TestResult
            {
                TestName = $"Test_{i}",
                Passed = i < count - 1, // Last one fails for variety
                ActualOutput = "4",
                Performance = perf
            });
        }
        return results;
    }
    
