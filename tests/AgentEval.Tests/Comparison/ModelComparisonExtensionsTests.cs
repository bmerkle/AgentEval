    [Fact]
    public void ToRankingsTable_OrdersModelsCorrectly()
    {
        // Arrange
        var result = CreateSampleComparisonResult();
        
        // Act
        var table = result.ToRankingsTable();
        var lines = table.Split('\n', StringSplitOptions.RemoveEmptyEntries);
        
        // Assert - verify table has header + separator + 2 model rows
        Assert.True(lines.Length >= 4);
        
        // First model row should have gold medal
        Assert.Contains("🥇", lines[2]);
        
        // Second model row should have silver medal
        Assert.Contains("🥈", lines[3]);
    }
    
